    <h1>Team Member Plugin Shortcodes</h1>
    <hr/>
	<h3>This plugin is support Bootstrap 5 only.</h3>
	<h3>1. You can use <mark>[team_members]</mark> code for the inner pages to show all Team Members.</h3>
    <h3>2. You can use <mark>[team_members profile="slug, slug2"]</mark> code for the inner pages to show specific profile Team Members.</h3>
    <h3>3. You can use <mark>[team_members show="2"]</mark> shortcode for show numbers of posts.</h3>
    <h3>4. You can use <mark>[team_members order="DESC"]</mark> shortcode for set posts orders. Exp: <mark>"ASC", "DESC"</mark></h3>
    <h3>5. You can use <mark>[team_members orderby="date"]</mark> shortcode for set posts orderby. Exp: <mark>"none", "ID", "title", "name", "menu_order".</mark></h3>
</div>